import { type NextPage } from "next";
import Head from "next/head";
import Carousel from "../foundations/Carousel";
import Link from "next/link";
import useSWR from "swr";
import { type AnsweredQuestion, QUESTIONS_KEY } from "./questions";
import useAuth from "../hooks/useAuth";
import { useEffect, useState } from "react";
import { api } from "../utils/api";
import { useRouter } from "next/router";

const Inspect: NextPage = () => {
  const { uuid, nickname } = useAuth();
  const { data: questions } = useSWR<AnsweredQuestion[]>(QUESTIONS_KEY);

  const [inspectedQuestions, setInspectedQuestions] = useState<
    AnsweredQuestion[]
  >([]);
  const [currentQuestion, setCurrentQuestion] =
    useState<AnsweredQuestion | null>(null);

  const inspected = inspectedQuestions
    .map((q) => q.id)
    .includes(Number(currentQuestion?.id));

  const [text, setText] = useState("");
  useEffect(() => {
    if (currentQuestion) {
      setText(currentQuestion.correctAnswer);
    }
  }, [currentQuestion]);

  const mutation = api.question.addOption.useMutation();
  const submit = () => {
    if (uuid && currentQuestion) {
      mutation.mutate({
        userUuid: uuid,
        questionId: currentQuestion.id,
        text,
      });
      setInspectedQuestions((state) => [...state, currentQuestion]);
    }
  };

  // 검수 완료
  const router = useRouter();
  useEffect(() => {
    if (questions && inspectedQuestions) {
      if (questions.length === inspectedQuestions.length) {
        void router.push("/score");
      }
    }
  }, [inspectedQuestions, questions]);

  return (
    <>
      <Head>
        <title>검수 - NooN</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <p>
          보이지 않아도 글을 듣고 어떤 상품인지 알 수 있다면 옷장에 담아주세요.
          혹시 방금 본 아이템들 중 설명이 부적절한 대체텍스트는 없었나요? 옷장에
          담기 전, {nickname}님이 직접 대체텍스트를 수정할 수 있어요!
        </p>
        <div className="carouselWrapper">
          {questions && (
            <Carousel
              fullWidth={833}
              fullHeight={650}
              items={new Array(9).fill(null).map((_, i) => {
                const question = questions[i % questions.length];
                return (
                  <img
                    className="image"
                    src={question?.url || ""}
                    alt={question?.correctAnswer || ""}
                    width={500}
                    height={500}
                    key={i}
                    style={{
                      opacity: inspectedQuestions
                        .map((q) => q.id)
                        .includes(Number(question?.id))
                        ? 0.1
                        : 1,
                    }}
                  />
                );
              })}
              emitCurrentIndex={(index) => {
                setCurrentQuestion(questions[index % questions.length]!);
              }}
            />
          )}
          <textarea
            className="input"
            maxLength={125}
            value={text}
            onChange={(e) => {
              setText(e.target.value);
            }}
            disabled={inspected}
            style={{
              opacity: inspected ? 0.5 : 1,
              pointerEvents: inspected ? "none" : "auto",
            }}
          />
          <button onClick={submit}>이 아이템 옷장에 집어넣기</button>
        </div>
        <Link href="/score">넘어 가기</Link>
      </main>
      <style jsx>{`
        .carouselWrapper {
          position: relative;
          display: flex;
          justify-content: center;
          align-items: center;
          flex-direction: column;

          max-width: 1000px;
          width: 100%;
          height: 100%;
          overflow: hidden;
        }
        .image {
          width: 100%;
          height: 100%;
          object-fit: cover;
          user-select: none;
          -webkit-user-drag: none;
          border-radius: 10px;
        }
        .input {
          z-index: 100;
          position: absolute;
          top: 50%;
          left: 50%;
          transform: translate(-50%, -50%);
          width: 400px;
          padding: 36px;

          background: rgba(255, 255, 255, 0.8);
          border: 1px solid #000000;
          border-radius: 10px;
          color: #7d7d7d;
          text-align: center;

          display: flex;
          align-items: center;

          white-space: pre-wrap;
          word-break: break-all;
          overflow: hidden;
          resize: none;
        }
      `}</style>
    </>
  );
};

export default Inspect;
